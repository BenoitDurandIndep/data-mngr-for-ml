CREATE TABLE `SYMBOL` (
	`SK_SYMBOL` INT NOT NULL AUTO_INCREMENT,
	`CODE` VARCHAR(20) NULL DEFAULT NULL,
	`NAME` VARCHAR(50) NULL DEFAULT NULL,
	`TYPE` VARCHAR(20) NULL DEFAULT NULL,
	`REGION` VARCHAR(20) NULL DEFAULT NULL,
	`CURRENCY` VARCHAR(20) NULL DEFAULT NULL,
	PRIMARY KEY (`SK_SYMBOL`) USING BTREE,
	INDEX `IX_SYMBOL_CODE` (`CODE`) USING BTREE
)
COLLATE='utf8_general_ci'
;
